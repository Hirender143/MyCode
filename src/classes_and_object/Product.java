package classes_and_object;

public class Product {

    private float price;
    private float qty;

    public void setPrice(float price) {
        this.price = price;
    }

    public float getPrice() {
        return price;
    }

    public void setQty(float qty) {
        this.qty = qty;
    }

    public float getQty() {
        return qty;
    }
}

